        <h1 >What is a bicycle crunch?</h1>
        <p>1. Lie on your back, lift your shoulders off the mat and raise both legs. <br>
           2. Bring one knee and the opposing elbow close to each other by crunching to one side, and fully extend the other leg. <br> 
           3. Return to the starting position and then crunch to the opposite side.
        </p>
        
        <h1 >What is a rowing?</h1>
        <p>1. Sit down on the seat and bend your knees. <br>
           2. Secure the footplate strap around the top of your shoelaces. <br>
           3. Hold the handle using an overhand grip. <br> 
           4. Engage your core and straighten your posture. <br> 
           5. Extend your arms and bend your knees to get into “the catch.” <br>
           6. Push off the footplate with your leg muscles.
        </p>
        
        <h1 >What is a lunge?</h1>
        <p>1. Bend the knees and lower your body until the back knee is a few inches from the floor. 
            At the bottom of the movement, the front thigh is parallel to the ground, the back knee points toward the floor, and your weight is evenly distributed between both legs. <br>
           2. Push back up to the starting position, keeping your weight on the heel of the front foot.
